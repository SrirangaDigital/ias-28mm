                <p class="title">Descriptor and indicator for sp<sup>2</sup>-hybridized carbon-based catalysts</p>
                <p class="abstract">
                    Although pristine graphene has not shown any potential as a catalyst, the presence of dopants and defects triggers its catalytic behaviour. This mystery behind the reactivity of doped defective graphene-based metal-free catalysts is unveiled using density functional theory. The occupancy of p<sub>z</sub>(π) electrons of C atoms increases near the dopant site due to back-donation mechanism in an N-doped graphene system and decreases in the case of B doping due to donation mechanism. A strong correlation has been identified between the activity of each C site with (i) the p<sub>z</sub> occupancy (considering occupied sates) and (ii) the value of p<sub>z</sub> projected density of states at the Fermi level. These parameters are proposed as catalytic descriptors for carbon-based catalysts. In addition, a new and simple catalytic indicator that helps to estimate the ideal sites for oxygen reduction reaction to occur by predicting the optimal values of the adsorption free energies of OH on the various active sites of boron and nitrogen-doped graphene has been identified by the speaker’s group. The origin of catalytic property of graphene-based catalysts, the competition between epoxy and enolate configuration on graphene, the bonding and back-bonding mechanism, the catalytic descriptor and indicator, and the origin of adsorption capabilities of DV-defect will be explained.
