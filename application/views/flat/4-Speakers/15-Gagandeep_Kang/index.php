                <p class="abstract">
                    Wherever vaccines have been widely introduced there has been an extraordinarily positive impact on mortality and morbidity. However, there is much to be done in India to ensure the full introduction of available vaccines in the national immunization programme. There are many challenges in the implementation of new vaccines for a large birth cohort, and there are challenges in implementing a life course approach to vaccination. Nonetheless, the situation is changing in India. The development and deployment of indigenous vaccines is exemplary and assurance that more such efforts will follow. The Indian vaccine industry, and indeed all of India, should take great pride in what has been done in the manufacture of vaccines in general. Yet, we need to ask ourselves if we could have gone ahead with faster development and licensure as well as with quicker implementation.

                    We need to be thinking about the future and what it holds for our capacity to develop vaccines, for our ability to go through regulatory processes and the making of decisions around vaccine use. These are challenging and complex, but strategic development and implementation requires that we divide the components in the pipeline into research, the ‘valley of death’ that needs to be crossed to take vaccine development into trials, manufacture, implementation and monitoring. We are beginning to take these steps, particularly for vaccines for outbreaks, with India seeking to play a key role as part of a cooperative and collaborative venture. End-to-end engagement requires multiple partners as well as coordination mechanisms, but it is feasible and we need to build on our experience to turn challenges in vaccines and public health into opportunities to protect our people against known and unknown preventable diseases.
