                <p class="abstract">
                    The unique properties of <i>Helicobacter pylori</i> DNA replication and cell division include but are not limited to the absence of a helicase loader DnaC, polar replisome formation and assembly of cell division proteins at the pole unlike <i>E. coli</i> where both the processes take place in the mid-cell region. These findings have helped to understand the assembly and dynamics of the <i>Hp</i>-replication machinery, leading to the identification of potential targets for therapy.
                </p>
                <p class="abstract">    
                    In eukaryotes, nuclear division is always followed by cytokinesis. However, in <i>P. falciparum</i>, during blood stage, the nucleus keeps on dividing until the schizont stage followed by cytokinesis yields new merozoites. A protein, PfORC1, that may link DNA replication with cell division has been identified. A possible CDK-like kinase involved in this process has also been identified. Further, putative replication initiation sites have been mapped and methods for specific intervention of parasite replication processes have been established.
